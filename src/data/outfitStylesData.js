// Función helper para obtener un outfit por ID
export const getOutfitById = (id) => {
  return OUTFIT_STYLES[id] || null;
};

// Función para obtener un outfit aleatorio
export const getRandomOutfit = () => {
  const keys = Object.keys(OUTFIT_STYLES);
  const randomKey = keys[Math.floor(Math.random() * keys.length)];
  return OUTFIT_STYLES[randomKey];
};

// Función para obtener outfits por categoría
export const getOutfitsByCategory = (category) => {
  const categoryData = OUTFIT_CATEGORIES[category];
  if (!categoryData) return [];
  return categoryData.styles.map(styleId => OUTFIT_STYLES[styleId]).filter(Boolean);
};
